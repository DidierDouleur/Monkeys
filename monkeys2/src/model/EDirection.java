package model;

/**
 * Enumération indiquant les différentes directions dans lesquelles il est possible de se déplacer.
 *
 */
public enum EDirection {
    /**
     * Gauche ou x-1.
     */
    LEFT,
    /**
     * Droite ou x+1.
     */
    RIGHT,
    /**
     * Haut ou y-1.
     */
    UP,
    /**
     * Bas ou y+1.
     */
    DOWN;
}
